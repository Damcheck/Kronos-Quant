export function getDemoDashboardActivity(limit = 40) {
	const items = [];
	const templates = [
		{ type: 'lifecycle', title: 'Strategy promoted', detail: '{name} moved from backtested → validated' },
		{ type: 'autopilot_job', title: 'Backtest completed', detail: '{name} on {symbol} {tf} — Sharpe {sharpe}' },
		{ type: 'signal', title: 'Long signal triggered', detail: '{name} fired buy on {symbol} at ${price}' },
		{ type: 'signal', title: 'Short signal triggered', detail: '{name} fired sell on {symbol} at ${price}' },
		{ type: 'data_ingestion', title: 'Data sync complete', detail: '{symbol} 1m candles updated — {count} new bars' },
		{ type: 'lifecycle', title: 'Strategy retired', detail: '{name} failed soak test — max DD exceeded threshold' },
		{ type: 'agent_action', title: 'Brain decision', detail: 'Promoted {name} to paper_trading based on robustness score {score}' },
		{ type: 'risk_alert', title: 'Risk threshold warning', detail: 'Portfolio correlation spike detected — {value}% across {count} strategies' },
		{ type: 'backtest_complete', title: 'Scan batch finished', detail: 'Scan #{scanId} tested {count} variants — {winners} passed filters' },
		{ type: 'lifecycle', title: 'Strategy discovered', detail: '{name} generated by evolution engine — pending backtest' },
		{ type: 'autopilot_job', title: 'Robustness check passed', detail: '{name} passed Monte Carlo + walk-forward validation' },
		{ type: 'strategy_promoted', title: 'Live deployment', detail: '{name} deployed to Hyperliquid testnet — paper trading active' },
	];

	for (let i = 0; i < Math.min(limit, 40); i++) {
		const tmpl = pickRandom(templates);
		const name = pickRandom(STRATEGY_NAMES);
		const symbol = pickRandom(SYMBOLS);
		const tf = pickRandom(TIMEFRAMES);
		const detail = tmpl.detail
			.replace('{name}', name)
			.replace('{symbol}', symbol)
			.replace('{tf}', tf)
			.replace('{sharpe}', (randomBetween(0.8, 2.5)).toFixed(2))
			.replace('{price}', randomBetween(100, 70000).toFixed(2))
			.replace('{count}', String(randomInt(50, 2400)))
			.replace('{winners}', String(randomInt(3, 28)))
			.replace('{scanId}', String(randomInt(100, 999)))
			.replace('{value}', (randomBetween(60, 92)).toFixed(1))
			.replace('{score}', (randomBetween(7.2, 9.6)).toFixed(1));

		items.push({
			type: tmpl.type,
			id: generateId(),
			ts: isoAgo(i * randomInt(3, 20)),
			title: tmpl.title,
			detail,
			strategy_id: generateId(),
		});
	}
	return items;
}

export function getDemoDashboardWinners(limit = 10) {
	const winners = [];
	for (let i = 0; i < limit; i++) {
		const sharpe = Math.round(randomBetween(1.2, 3.5) * 100) / 100;
		const totalReturn = Math.round(randomBetween(15, 180) * 100) / 100;
		const winRate = Math.round(randomBetween(48, 72) * 100) / 100;
		const maxDD = Math.round(randomBetween(3, 18) * 100) / 100;
		winners.push({
			id: generateId(),
			strategy_name: STRATEGY_NAMES[i % STRATEGY_NAMES.length],
			display_name: null,
			symbol: pickRandom(SYMBOLS),
			timeframe: pickRandom(TIMEFRAMES),
			sharpe_ratio: sharpe,
			total_return: totalReturn,
			monthly_return_pct: Math.round(randomBetween(2, 12) * 100) / 100,
			annualized_return_pct: Math.round(randomBetween(30, 200) * 100) / 100,
			max_drawdown: maxDD,
			win_rate: winRate,
			total_trades: randomInt(80, 1200),
			profit_factor: Math.round(randomBetween(1.3, 3.2) * 100) / 100,
			sortino_ratio: Math.round(sharpe * randomBetween(1.1, 1.6) * 100) / 100,
			calmar_ratio: Math.round((totalReturn / maxDD) * 100) / 100,
			source: pickRandom(['scanner', 'evolution', 'manual', 'crucible']),
			scan_id: generateId(),
			lifecycle_strategy_id: generateId(),
			tier: pickRandom(['S', 'A', 'B']),
			mini_equity: generateEquityCurve(30, 10000, 0.003),
		});
	}
	// Sort by sharpe descending
	winners.sort((a, b) => b.sharpe_ratio - a.sharpe_ratio);
	return winners;
}

