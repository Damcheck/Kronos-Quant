// ── Helpers ──────────────────────────────────────────────────────────

function randomBetween(min: number, max: number): number {
	return Math.random() * (max - min) + min;
}

function randomInt(min: number, max: number): number {
	return Math.floor(randomBetween(min, max + 1));
}

function pickRandom<T>(arr: T[]): T {
	return arr[Math.floor(Math.random() * arr.length)];
}

function generateId(): string {
	return Math.random().toString(36).slice(2, 10) + Date.now().toString(36);
}

function isoAgo(minutesAgo: number): string {
	return new Date(Date.now() - minutesAgo * 60_000).toISOString();
}

function generateEquityCurve(points: number, startVal: number, drift: number): number[] {
	const curve: number[] = [startVal];
	for (let i = 1; i < points; i++) {
		const change = curve[i - 1] * (drift + (Math.random() - 0.48) * 0.02);
		curve.push(Math.max(curve[i - 1] + change, startVal * 0.7));
	}
	return curve.map(v => Math.round(v * 100) / 100);
}

